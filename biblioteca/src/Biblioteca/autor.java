package Biblioteca;

public class autor {

	
	public String nombre;
	public int id_autor;
	
	public autor() {}
	
	public autor(String nombre, int id_autor) {
		
	}

	public String getNombre() {
		return nombre;
	}

	public void setNombre(String nombre) {
		this.nombre = nombre;
	}

	public int getId_autor() {
		return id_autor;
	}

	public void setId_autor(int id_autor) {
		this.id_autor = id_autor;
	}
	
	
}
